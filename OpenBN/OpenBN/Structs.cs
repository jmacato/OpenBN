namespace OpenBN
{
    public struct Size
    {
        public int W;
        public int H;

        public Size(int W, int H) : this()
        {
            this.W = W;
            this.H = H;
        }
    }

    public struct Point
    {
        public int X;
        public int Y;

        public Point(int X, int Y) : this()
        {
            this.X = X;
            this.Y = Y;
        }

    }

}
